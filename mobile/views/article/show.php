        <div>　　中国土壤污染现状<br>
<br> 　　1、土壤的重金属污染 <br> 　　随着工业、城市污染的加剧和农用化学物质种类、数量的增加，土壤重金属污染日益严重，污染程度在加剧，面积在逐年扩大.重金属污染物在土壤中移动差、滞留时间长、不能被微生物降解，并可经水、植物等介质最终影响人类健康。 <br>
<br> 　　2、土壤的有机污染 <br> 　　目前我国土壤的有机污染十分严重，且对农产品和人体健康的影响已开始显现.如我国从1959年起在长江中下游地区用五氯酚钠防治血吸虫病，其中的杂质二恶英已造成区域二恶英类污染，洞庭湖、鄱阳湖底泥中的二恶英含量很高.有机氯农药已禁用了近20年，土壤中的残留量已大大降低，但检出率仍很高。 <br>
<br> 　　3、有机污染物在生物体内的富集 <br> 　　由于土壤是植物和一些生物的营养来源，所以土壤中的有机污染物会通过食物链发生传递和迁移，目前动物和人类自身都遭受有机污染物的污染和威胁. <br>
<br> 　　4、土壤的放射污染 <br>
<p>
    近年来，随着核技术在工农业、医疗、地质、科研等各领域的广泛应用，越来越多的放射污染物进入到土壤中，这些放射污染物除可直接危害人体外，还可以通过生物链和食物链进入人体，在人体内产生内照射，损伤人体组织细胞，引起肿瘤、白血病和遗传障碍等疾病。
</p>
